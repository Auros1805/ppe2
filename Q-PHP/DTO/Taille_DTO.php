
    <?php

    class Taille_DTO
    {
        private $idTaille;
        private $nomTaille;
        private $prixTaille;

        public getIdTaille() {
            return $idTaille;
        }
    
        public void setIdTaille( $idTaille) {
            $this.$idTaille = $idTaille;
        }
    
        public String getNomTaille() {
            return $nomTaille;
        }
    
        public void setNomTaille( $nomTaille) {
            $this.$nomTaille = $nomTaille;
        }
    
        public getPrixTaille() {
            return $prixTaille;
        }
    
        public void setPrixTaille( $prixTaille) {
            $this.$prixTaille = $prixTaille;
        }
    }
?>